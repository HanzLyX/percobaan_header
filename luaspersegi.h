#include <stdio.h>
#include <conio.h>
#include <stdlib.h>
#include <math.h>

typedef struct {
    float s;
} prsg;

float Luaspersegi(prsg sisi, float l);
float Kelilingpersegi(prsg sisi, float k);

float Luaspersegi(prsg sisi, float l){
    return l = 2 * sisi.s;
}

float Kelilingpersegi(prsg sisi, float k){
    return k = 4 * sisi.s;
}

